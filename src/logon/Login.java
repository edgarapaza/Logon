    public void IniciarSesion() throws SQLException{
       int valor=0;
        try{
            con.conectar();
            
            ResultSet rs =null; 
                    rs=con.consulta("SELECT count(*) as total FROM Empleados WHERE Emp_usuario='"+TxtUser.getText()+"' AND Emp_clave='"+Txtclave.getText()+"';");
            valor = rs.getInt(1);
            JOptionPane.showMessageDialog(PanelLogon, "Este  valor: "+valor);
            
            
                if(valor == 1)
                {
                   new Principal().setVisible(true);
                    dispose();

                }
                else
                {
                    JOptionPane.showMessageDialog(null,"Usuario no registrado");
                }
                
            con.cierraConexion();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(rootPane, "Error de consulta. " + e.getMessage());
        }
        
    }
